# Python NumPy random module

# NumPy random module is used to generate random numbers in Python.
# It is a sub-module of NumPy.

# PRNGs: Pseudo Random Number Generators
# PRNGs are algorithms that use mathematical formulas to produce sequences 
# of random numbers.

# NumPy random module have many functions to generate random numbers.
# Some of the functions are:
# 1. rand() - It is used to generate random numbers between 0 and 1.
# 2. randint() - It is used to generate random integers.
# 3. randn() - It is used to generate random numbers from a normal distribution.
# 4. random() - It is used to generate random numbers from a uniform distribution.

# Distribution:
# 1. Normal Distribution:  np.random.normal()
# 2. Uniform Distribution: np.random.uniform()
# 3. Binomial Distribution: np.random.binomial()
# 4. Poisson Distribution: np.random.poisson()
# 5. Exponential Distribution: np.random.exponential()
